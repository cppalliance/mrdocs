#ifndef MRDOCS_SHARE_HEADERS_LIBC_STUBS_ISO646_H
#define MRDOCS_SHARE_HEADERS_LIBC_STUBS_ISO646_H

#ifndef __cplusplus
// Operator macros (C95)
#ifndef and
#define and     &&
#endif

#ifndef and_eq
#define and_eq  &=
#endif

#ifndef bitand
#define bitand  &
#endif

#ifndef bitor
#define bitor   |
#endif

#ifndef compl
#define compl   ~
#endif

#ifndef not
#define not     !
#endif

#ifndef not_eq
#define not_eq  !=
#endif

#ifndef or
#define or      ||
#endif

#ifndef or_eq
#define or_eq   |=
#endif

#ifndef xor
#define xor     ^
#endif

#ifndef xor_eq
#define xor_eq  ^=
#endif

#endif // __cplusplus

#endif // MRDOCS_SHARE_HEADERS_LIBC_STUBS_ISO646_H
